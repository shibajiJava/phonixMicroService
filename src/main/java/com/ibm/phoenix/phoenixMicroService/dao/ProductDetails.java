package com.ibm.phoenix.phoenixMicroService.dao;

public class ProductDetails {

	private String segment;	
	private String segmentName;
	private String family; 
	private String familyNameClass;
	private String className;
	private String commodity;
	private String commodityName;
	
	
	public String getSegment() {
		return segment;
	}
	
	public void setSegment(String segment) {
		this.segment = segment;
	}
	
	public String getSegmentName() {
		return segmentName;
	}
	
	public void setSegmentName(String segmentName) {
		this.segmentName = segmentName;
	}
	
	public String getFamily() {
		return family;
	}
	
	public void setFamily(String family) {
		this.family = family;
	}
	
	public String getFamilyNameClass() {
		return familyNameClass;
	}
	
	public void setFamilyNameClass(String familyNameClass) {
		this.familyNameClass = familyNameClass;
	}
	
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getCommodity() {
		return commodity;
	}
	public void setCommodity(String commodity) {
		this.commodity = commodity;
	}
	public String getCommodityName() {
		return commodityName;
	}
	public void setCommodityName(String commodityName) {
		this.commodityName = commodityName;
	}
	
	
	
}
